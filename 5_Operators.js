// arithmetic operators: +,-,*,/,%,++,--;
let a = 5, b = 10;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a++);
console.log(a);
console.log(++a);
console.log(a);
console.log(b--);
console.log(b);
console.log(--b);
console.log(b);

// comparison opeartors: ==, ===, !=, >, >=, <, <=;
let i = 5, j = 10, k = "5";
const z = i;

console.log(i == k);
console.log(i === k);
console.log(i == z);
console.log(i === z);
console.log(i != j);
console.log(i != z);
console.log(i > j);
console.log(i < j);
console.log(i >= j);
console.log(i <= j);


// Assignment operators: =, +=, -=, *=, /=, %=;
